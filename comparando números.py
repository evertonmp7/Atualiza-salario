número_1 = float(input('Digite um número: '))
número_2 = float(input('Digite outro número: '))

if número_1 == número_2:
    print('Os números são iguais')

elif número_1 > número_2:
    print('O primeiro valor é maior: ', número_1)
    print('O segundo valor é menor: ', número_2)

elif número_1 < número_2:
    print('O segundo valor é maior: ', número_2)
    print('O primeiro valor é menor: ', número_1)

    
                 
